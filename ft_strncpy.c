#include "libft.h"

char *strncpy(char *dst, char *src, size_t len)
{
	int i;

	i = 0;
	while((src[i]) && (i <= len))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
