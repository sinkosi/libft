#include "libft.h"

void	ft_putstr(const char *str)
{
	size_t i;

	i = 0;
	while (str[i] != '\0')
	{
		if (ft_isprint(str[i] == 0))
		{
			return (0);
		}
		i++;
	}
	while (*str)
		write(1, str++, 1);
}
